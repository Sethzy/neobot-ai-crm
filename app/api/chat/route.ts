/**
 * Browser chat route for Managed Agents.
 * Streams one turn directly from `runManagedAgent()`.
 *
 * @module app/api/chat/route
 */
import { createUIMessageStreamResponse } from "ai";
import { z } from "zod";

import { authenticateAndParseBody, jsonError } from "@/lib/api/route-helpers";
import { allowedModelIds, DEFAULT_CHAT_MODEL } from "@/lib/ai/models";
import { generateTitleFromUserMessage } from "@/lib/ai/title";
import { resolveClientId } from "@/lib/chat/client-id";
import { extractUserInput } from "@/lib/chat/extract-user-input";
import {
  resumeManagedAgentFromApproval,
  runManagedAgent,
} from "@/lib/managed-agents/adapter";
import { getAnthropicClient } from "@/lib/managed-agents/anthropic-client";
import { checkRateLimit } from "@/lib/rate-limit";

export const runtime = "nodejs";
export const maxDuration = 300;

const NEW_THREAD_TITLE = "New Chat";

const requestSchema = z.object({
  id: z.string().min(1),
  messages: z
    .array(
      z.object({
        id: z.string().optional(),
        role: z.string(),
        parts: z.array(z.unknown()).optional(),
      }),
    )
    .min(1),
  selectedChatModel: z.string().optional(),
});

interface ApprovalContinuation {
  approvalId: string;
  approved: boolean;
  denyMessage?: string;
}

function getUrlPath(value: string): string {
  try {
    return new URL(value).pathname;
  } catch {
    return value;
  }
}

/**
 * Checks the trailing messages for a tool approval response generated by useChat.
 */
function extractApprovalContinuation(
  messages: Array<{ parts?: unknown[] }>,
): ApprovalContinuation | null {
  const startIndex = Math.max(0, messages.length - 2);

  for (let index = messages.length - 1; index >= startIndex; index -= 1) {
    const message = messages[index];
    for (const part of message.parts ?? []) {
      if (typeof part !== "object" || part === null || !("type" in part)) {
        continue;
      }

      const typedPart = part as Record<string, unknown>;
      if (
        typeof typedPart.type !== "string" ||
        !typedPart.type.startsWith("tool-")
      ) {
        continue;
      }

      const state =
        typeof typedPart.state === "string" ? typedPart.state : null;
      if (state !== "approval-responded" && state !== "output-denied") {
        continue;
      }

      const approvalId =
        typeof typedPart.toolCallId === "string" ? typedPart.toolCallId : null;
      if (!approvalId) {
        continue;
      }

      const approval =
        typeof typedPart.approval === "object" && typedPart.approval !== null
          ? (typedPart.approval as { approved?: boolean; denyMessage?: string })
          : undefined;

      return {
        approvalId,
        approved: state !== "output-denied" && approval?.approved === true,
        ...(typeof approval?.denyMessage === "string"
          ? { denyMessage: approval.denyMessage }
          : {}),
      };
    }
  }

  return null;
}

/**
 * Accepts one browser chat turn and streams the managed-agent response.
 */
export async function POST(request: Request): Promise<Response> {
  const t0 = performance.now();

  const requestResult = await authenticateAndParseBody(request, requestSchema);
  if (requestResult.kind === "error") {
    return requestResult.response;
  }
  const tParse = performance.now();
  const tAuth = performance.now();

  const selectedChatModel = requestResult.body.selectedChatModel ?? DEFAULT_CHAT_MODEL;
  if (!allowedModelIds.has(selectedChatModel)) {
    return jsonError("Invalid selected chat model.", 400);
  }

  const approval = extractApprovalContinuation(requestResult.body.messages);
  if (approval) {
    const approvalResult = await resumeManagedAgentFromApproval({
      anthropic: getAnthropicClient(),
      supabase: requestResult.supabase,
      clientId: await resolveClientId(requestResult.supabase, requestResult.userId),
      approvalId: approval.approvalId,
      approved: approval.approved,
      denyMessage: approval.denyMessage,
    });

    if (approvalResult.status === "missing") {
      return jsonError("Approval not found.", 404);
    }

    if (approvalResult.status === "already_resolved") {
      return jsonError("Approval already resolved.", 409);
    }

    if (approvalResult.status === "error") {
      return jsonError(approvalResult.error, 500);
    }

    return createUIMessageStreamResponse({ stream: approvalResult.stream as never });
  }

  // Rate limit + client ID resolve in parallel — both depend only on auth.userId.
  const [rateLimitResult, clientId] = await Promise.all([
    checkRateLimit(`chat:${requestResult.userId}`, 30, 60),
    resolveClientId(requestResult.supabase, requestResult.userId),
  ]);
  if (!rateLimitResult.allowed) {
    return new Response(
      JSON.stringify({
        error: "Rate limit exceeded. Please wait before sending more messages.",
      }),
      {
        status: 429,
        headers: {
          "Content-Type": "application/json",
          "Retry-After": String(rateLimitResult.retryAfter ?? 60),
        },
      },
    );
  }
  const tClientId = performance.now();

  const lastMessage = requestResult.body.messages[requestResult.body.messages.length - 1];
  const { text, fileParts } = extractUserInput({
    parts: lastMessage.parts ?? [],
  });

  if (fileParts.length > 0) {
    console.info("[api/chat] extracted file parts", {
      threadId: requestResult.body.id,
      messageId: lastMessage.id ?? null,
      textLength: text?.length ?? 0,
      fileParts: fileParts.map((filePart) => ({
        filename: filePart.filename ?? null,
        mediaType: filePart.mediaType,
        storagePath: filePart.storagePath ?? null,
        urlPath: getUrlPath(filePart.url),
      })),
    });
  }

  if (!text && fileParts.length === 0) {
    return jsonError("Message must contain text or files.", 400);
  }

  const threadResult = await requestResult.supabase
    .from("conversation_threads")
    .select("thread_id, title, session_id, chat_model")
    .eq("thread_id", requestResult.body.id)
    .eq("client_id", clientId)
    .eq("is_archived", false)
    .maybeSingle();

  if (threadResult.error) {
    return jsonError("Failed to process request.", 500);
  }
  const existingThread = threadResult.data;
  const existingSessionId = existingThread?.session_id ?? null;
  // Lock the model to the thread once created. The picker only chooses
  // the model for new threads; existing threads always use the value
  // stamped on the row at creation time.
  const threadChatModel = existingThread?.chat_model ?? selectedChatModel;
  let clientProfile: string | null = null;
  let userPreferences: string | null = null;

  if (!existingSessionId) {
    const clientContextResult = await requestResult.supabase
      .from("clients")
      .select("client_profile, user_preferences")
      .eq("client_id", clientId)
      .single();

    if (clientContextResult.error) {
      return jsonError("Failed to process request.", 500);
    }

    clientProfile = clientContextResult.data?.client_profile ?? null;
    userPreferences = clientContextResult.data?.user_preferences ?? null;
  }
  const tParallelLookup = performance.now();

  let threadTitle: string | null = null;
  let titlePromise: Promise<string> | null = null;
  if (!existingThread) {
    const { error: insertError } = await requestResult.supabase
      .from("conversation_threads")
      .insert({
        thread_id: requestResult.body.id,
        client_id: clientId,
        title: NEW_THREAD_TITLE,
        chat_model: selectedChatModel,
      });
    if (insertError) {
      return jsonError("Failed to create thread.", 500);
    }
    threadTitle = NEW_THREAD_TITLE;
    if (text?.trim().length) {
      titlePromise = generateTitleFromUserMessage(text);
    }
  } else {
    threadTitle = existingThread.title;
  }

  console.info("[api/chat] route setup timing (ms)", {
    parse: Math.round(tParse - t0),
    auth: Math.round(tAuth - tParse),
    rateLimitAndClientId: Math.round(tClientId - tAuth),
    threadAndContext: Math.round(tParallelLookup - tClientId),
    total: Math.round(tParallelLookup - t0),
  });

  const tPreAdapter = performance.now();
  const stream = await runManagedAgent({
    anthropic: getAnthropicClient(),
    supabase: requestResult.supabase,
    clientId,
    threadId: requestResult.body.id,
    input: text ?? "",
    fileParts,
    userMessageSourceId: lastMessage.id,
    clientProfile,
    userPreferences,
    threadTitle,
    existingSessionId,
    generatedTitlePromise: titlePromise,
    selectedChatModel: threadChatModel,
  });
  const tPostAdapter = performance.now();

  console.info("[api/chat] full route timing (ms)", {
    routeSetup: Math.round(tParallelLookup - t0),
    adapterInit: Math.round(tPostAdapter - tPreAdapter),
    totalToStream: Math.round(tPostAdapter - t0),
  });

  return createUIMessageStreamResponse({ stream: stream as never });
}
